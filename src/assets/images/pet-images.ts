export type PetImageKey = keyof typeof PetImages

// Helper function to get pet image by name
export const getPetImage = (name: string) => {
  const key = name.toLowerCase().replace(/[^a-z0-9]/g, "") as PetImageKey
  return PetImages[key] || PetImages.bulbasaur // fallback
}